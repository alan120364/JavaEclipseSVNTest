package adder;

public class Adder {
	
	/**
	 * Instantiates a new adder.
	 */
	public Adder(){
		
	}
	/**
	 * Add2 integer.
	 *
	 * @param a the 1st integer input
	 * @param b the 2nd integer input
	 * @return the sum
	 */
	public int add2Integer(int a, int b){
		return a + b;
	}
	
	
	/**
	 * Add3 integer.
	 *
	 * @param a the 1st integer input
	 * @param b the 2nd integer input
	 * @param c the 3rd integer input
	 * @return the sum
	 */
	public int add3Integer(int a, int b, int c){
		return a + b + c;
	}
}
